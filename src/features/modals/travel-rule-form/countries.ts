type MatchResult = {
  country: Country
  restNumber: string
} | null

export function getCountryByPhoneNumber(phone: string): MatchResult {
  if (!phone) return null
  const normalized = phone.replace(/[^\d+]/g, '') // —É–¥–∞–ª—è–µ–º –ø—Ä–æ–±–µ–ª—ã, —Å–∫–æ–±–∫–∏ –∏ –ø—Ä–æ—á–µ–µ
  const possibleCodes = Object.entries(countriesData)
    .map(([code, data]) => ({
      ...data,
      phoneCode: data.phoneCode.replace(/\D/g, ''), // —É–±–∏—Ä–∞–µ–º '+' –µ—Å–ª–∏ –µ—Å—Ç—å
    }))
    .sort((a, b) => b.phoneCode.length - a.phoneCode.length) // –∏—â–µ–º —Å–∞–º—ã–π –¥–ª–∏–Ω–Ω—ã–π —Å–æ–≤–ø–∞–¥–∞—é—â–∏–π –∫–æ–¥

  for (const country of possibleCodes) {
    if (normalized.startsWith(`+${country.phoneCode}`)) {
      return {
        country: {
          ...country,
          phoneCode: `+${country.phoneCode}`, // –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å –ø–ª—é—Å–æ–º
        },
        restNumber: normalized.slice(country.phoneCode.length + 1),
      }
    }
  }

  return null
}
